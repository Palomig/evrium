// Функция получения данных о главе
function getChapter($class, $chapter_num) {
    global $chapters;
    if (isset($chapters[$class]['chapters'][$chapter_num])) {
        return $chapters[$class]['chapters'][$chapter_num];
    }
    return null;
}

// Функция получения всех глав класса
function getClassChapters($class) {
    global $chapters;
    if (isset($chapters[$class]['chapters'])) {
        return $chapters[$class]['chapters'];
    }
    return [];
}

// Функция получения данных о теме
function getTopic($class, $chapter_num, $topic_num) {
    global $chapters;
    if (isset($chapters[$class]['chapters'][$chapter_num]['topics'][$topic_num])) {
        return $chapters[$class]['chapters'][$chapter_num]['topics'][$topic_num];
    }
    return null;
}
